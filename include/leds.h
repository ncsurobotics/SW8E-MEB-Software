#ifndef LEDS_H
#define LEDS_H

//---------------
// COLORS
//---------------
/* Colors are defined in the format (0xRRGGBB) */
#define BLACK       (0x000000) // Turns the LED OFF
#define WHITE       (0xFFFFFF)
#define RED         (0xFF0000)
#define YELLOW      (0xF08000)
#define GREEN       (0x00FF00)
#define BLUE        (0x0000FF)
#define MAGENTA     (0xFF005E)

#endif